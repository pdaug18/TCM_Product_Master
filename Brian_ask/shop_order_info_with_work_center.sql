	RTO_HR_MACH_SR,
	RTOWC,
	HR_LABOR_SF,
	SOPERWC,
	ID_RTE,
	SOWC,
	MACH_SR_LABOR_SF,
	SHOP_ORDER_LOCATION,
	O_QTY_CMPL,
	O_QTY_ORD,
	ID_ITEM_PAR,
	DATE_START_OPER_1ST,
	TIME_START_OPER_1ST,
	STAT_REC_SO,
	STAT_REC_OPER,
	DATE_ADD,
	DATE_DUE_ORD,
	"ShopOrder#",
	SUFX_SO,
	SH_SO_AND_SUFX,
	DESCR_ITEM_1,
	DESCR_ITEM_2,
	ID_OPER,
	SA_SO_AND_SUFX,
	DATE_APPROVED,
	DATETIME_APPROVED,
	TIME_APPROVED,
	FLAG_STK,
	FLAG_SOURCE,
	MATERIAL_CODE,
	ID_PLANNER,
	STKLIST_ITEM,
	LEVEL_ROP,
	SOFROMPREPROD,
	SUM_ACTUAL_MINS,
	SUM_EARNED_MINS,
	SUM_AVAIL_MINS,
	SUM_INDIR_MINS,
	SUM_SAMPLE_MINS,
	WORK_CENTER_LOCATION,
	EMPLOYEE_LOCATION,
	WORK_CENTER_DESCRIPTION,
	FIRST_SHIFT_FT,
	SECOND_SHIFT_FT,
	SECOND_SHIFT_PT,
	INACTIVE,
	FIRST_SHIFT_FT_EMPLOYEES,
	SECOND_SHIFT_FT_EMPLOYEES,
	SECOND_SHIFT_PT_EMPLOYEES,
	FIRST_SHIFT_TEAMS,
	SECOND_SHIFT_TEAMS
) as
SELECT
    soi.*,
    wceff.SUM_ACTUAL_MINS,
	wceff.SUM_EARNED_MINS,
	wceff.SUM_AVAIL_MINS,
	wceff.SUM_INDIR_MINS,
	wceff.SUM_SAMPLE_MINS,
    wceff.WORK_CENTER_LOCATION,
	wceff.EMPLOYEE_LOCATION,
    wceff.WORK_CENTER_DESCRIPTION,
    wceff.FIRST_SHIFT_FT,
    wceff.SECOND_SHIFT_FT,
    wceff.SECOND_SHIFT_PT,
    wceff.INACTIVE,
    wceff.FIRST_SHIFT_FT_EMPLOYEES,
    wceff.SECOND_SHIFT_FT_EMPLOYEES,
    wceff.SECOND_SHIFT_PT_EMPLOYEES,
    wceff.FIRST_SHIFT_TEAMS,
    wceff.SECOND_SHIFT_TEAMS

FROM SILVER_DATA.TCM_SILVER.SHOP_ORDER_INFO soi
LEFT JOIN SILVER_DATA.TCM_SILVER.WORK_CENTER_MINUTES_EMPLOYEE_SUMMARY wceff
    ON TRIM(soi.SOWC) = TRIM(wceff.WORK_CENTER)
   AND TRIM(soi.SHOP_ORDER_LOCATION) = TRIM(wceff.WORK_CENTER_LOCATION)

WHERE soi.SOWC NOT IN (
    '900', '950', '1100', '1200', '1300', '1400',
    '7699', '7950', '8000', '7990'
);